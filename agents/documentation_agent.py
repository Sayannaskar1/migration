def _highlight_sql_text(sql: str) -> str:
    """Server-side SQL syntax highlighting with inline styles (no CSS dependency)."""
    if not sql:
        return ""
    import re

    def _esc(text: str) -> str:
        return text.replace("&", "&amp;").replace("<", "&lt;").replace(">", "&gt;").replace('"', "&quot;").replace("'", "&#39;")

    mapping: dict[str, str] = {}
    counter = 0

    def _ph() -> str:
        nonlocal counter
        k = "\x01\x02" + "\x03" * (counter + 1) + "\x02\x01"
        counter += 1
        return k

    def _set(m, cls: str) -> str:
        k = _ph()
        style = _SQL_STYLES.get(cls, '')
        mapping[k] = f'<span style="{style}">{_esc(m.group(1))}</span>'
        return k

    html = sql

