    html = re.sub(r'\b(STRING|INT|INTEGER|BIGINT|SMALLINT|TINYINT|BOOLEAN|DATE|TIMESTAMP|TIMESTAMP_NTZ|TIMESTAMP_LTZ|TIMESTAMP_TZ|DECIMAL|NUMERIC|NUMBER|FLOAT|DOUBLE|REAL|BINARY|VARBINARY|BYTES|ARRAY|STRUCT|MAP|VARIANT|OBJECT|GEOGRAPHY|GEOMETRY|INTERVAL)\b', lambda m: _set(m, 'sql-type'), html, flags=re.IGNORECASE)
