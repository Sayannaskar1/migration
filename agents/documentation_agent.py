def _type_display_name(raw: str) -> str:
    return raw.replace("_", " ").title()


OBJECT_TYPE_LABELS = {}
for _attr_name in dir(ObjectType):
    if _attr_name.startswith("_") or _attr_name == "UNKNOWN":
        continue
    _val = getattr(ObjectType, _attr_name)
    if isinstance(_val, str):
        OBJECT_TYPE_LABELS[_val] = _type_display_name(_val)
OBJECT_TYPE_LABELS[ObjectType.UNKNOWN] = "Unknown"


def _complexity_level(obj, result: ValidationResult = None) -> tuple[str, int]:
    base = {"table": 1, "view": 2, "function": 3, "procedure": 4}.get(obj.object_type, 3)
    feature_count = len(detect_snowflake_features(obj.raw_sql))
    base += feature_count
    if result:
        base += len(result.warnings) * 1
        base += len(result.issues) * 2
        base += len(result.errors) * 3
        if result.status == "ARCHITECTURAL CHANGE":
            base += 2
    if base <= 2:
        return "Easy", base
    elif base <= 4:
        return "Medium", base
    elif base <= 7:
        return "Hard", base
    else:
        return "Expert", base


def _h(text: str) -> str:
    return text.replace("&", "&amp;").replace("<", "&lt;").replace(">", "&gt;").replace('"', "&quot;").replace("'", "&#39;")


def _build_svg_dep_graph(dep_graph: DependencyGraph) -> str:
    dep_graph.has_cycles()
    ordered = dep_graph.get_deployment_order()
    nodes_by_name: dict[str, dict] = {}
    for obj in ordered:
        n = obj.name.lower()
        deps = dep_graph.edges.get(n, [])
        rev = dep_graph.reverse_edges.get(n, [])
        nodes_by_name[n] = {"name": obj.name, "type": obj.object_type, "deps": deps, "rev": rev}
    if not nodes_by_name:
        return "<p>No dependencies detected.</p>"

    TYPE_ORDER = [
        "schema",
        "table", "external_table", "iceberg_table", "dynamic_table",
        "materialized_view", "view",
        "function",
        "procedure",
        "stream", "task", "pipe", "stage",
    ]
    TYPE_COLORS = {
        "schema":              { "bg": "#eef2ff", "border": "#6366f1", "text": "#4338ca", "badge": "#6366f1", "colbg": "#f4f6ff" },
        "table":               { "bg": "#f0fdf4", "border": "#22c55e", "text": "#15803d", "badge": "#22c55e", "colbg": "#f4fdf6" },
        "external_table":      { "bg": "#f0fdf4", "border": "#22c55e", "text": "#15803d", "badge": "#22c55e", "colbg": "#f4fdf6" },
        "iceberg_table":       { "bg": "#f0fdf4", "border": "#22c55e", "text": "#15803d", "badge": "#22c55e", "colbg": "#f4fdf6" },
        "dynamic_table":       { "bg": "#f0fdf4", "border": "#22c55e", "text": "#15803d", "badge": "#22c55e", "colbg": "#f4fdf6" },
        "materialized_view":   { "bg": "#ecfeff", "border": "#06b6d4", "text": "#0e7490", "badge": "#06b6d4", "colbg": "#f2fdfe" },
        "view":                { "bg": "#ecfeff", "border": "#06b6d4", "text": "#0e7490", "badge": "#06b6d4", "colbg": "#f2fdfe" },
        "function":            { "bg": "#fffbeb", "border": "#f59e0b", "text": "#b45309", "badge": "#f59e0b", "colbg": "#fffdf3" },
        "procedure":           { "bg": "#fef2f2", "border": "#ef4444", "text": "#b91c1c", "badge": "#ef4444", "colbg": "#fef6f6" },
        "stream":              { "bg": "#fdf2f8", "border": "#ec4899", "text": "#be185d", "badge": "#ec4899", "colbg": "#fef6fa" },
        "task":                { "bg": "#f3e8ff", "border": "#8b5cf6", "text": "#6d28d9", "badge": "#8b5cf6", "colbg": "#f8f4ff" },
        "pipe":                { "bg": "#f0fdfa", "border": "#14b8a6", "text": "#0f766e", "badge": "#14b8a6", "colbg": "#f4fdfb" },
        "stage":               { "bg": "#fff7ed", "border": "#f97316", "text": "#c2410c", "badge": "#f97316", "colbg": "#fffbf4" },
    }
    DEFAULT_COLOR = { "bg": "#f8fafc", "border": "#94a3b8", "text": "#475569", "badge": "#94a3b8", "colbg": "#f8f8f8" }

    # Map types to columns
    type_col: dict[str, int] = {}
    col = 0
    for t in TYPE_ORDER:
        if any(info["type"] == t for info in nodes_by_name.values()):
            type_col[t] = col
            col += 1

    column_nodes: dict[int, list[str]] = {}
    for name, info in nodes_by_name.items():
        t = info["type"]
        c = type_col.get(t, -1)
        if c < 0:
            c = max(type_col.values()) + 1 if type_col else 0
            type_col[t] = c
        column_nodes.setdefault(c, []).append(name)

    if not column_nodes:
        return "<p>No dependencies detected.</p>"

    used_cols = sorted(column_nodes.keys())
    total_cols = len(used_cols)

    box_w, box_h = 220, 42
    col_gap = 50
    row_gap = 28
    pad_x = 50
    pad_y = 60
    header_h = 30

    col_width = box_w + col_gap
    max_row = max(len(v) for v in column_nodes.values()) if column_nodes else 1
    total_w = total_cols * col_width + pad_x * 2
    total_h = header_h + max_row * (box_h + row_gap) + pad_y + header_h
    total_w = max(total_w, 750)
    total_h = max(total_h, 300)

    svg_parts = [f'<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 {total_w} {total_h}" style="width:100%;height:auto;background:#fff;border-radius:8px;" class="dep-svg">']
    edge_layer: list[str] = []
    node_layer: list[str] = []

    # Column backgrounds
    col_headers: list[str] = []
    for ci, c in enumerate(used_cols):
        cx = pad_x + ci * col_width
        color = DEFAULT_COLOR
        for t in TYPE_ORDER:
            if type_col.get(t) == c:
                color = TYPE_COLORS.get(t, DEFAULT_COLOR)
                break
        col_headers.append(f'<rect x="{cx}" y="{pad_y}" width="{box_w + col_gap}" height="{total_h - pad_y * 2}" fill="{color["colbg"]}" rx="4"/>')

    node_positions: dict[str, tuple[float, float]] = {}
    for ci, c in enumerate(used_cols):
        items = column_nodes[c]
        items.sort(key=lambda n: nodes_by_name[n]["name"])
        x = pad_x + ci * col_width + col_gap / 2
        y_start = pad_y + header_h + (total_h - pad_y - header_h - pad_y - header_h - len(items) * (box_h + row_gap)) / 2 + row_gap / 2
        if y_start < pad_y + header_h + 4:
            y_start = pad_y + header_h + 4
        for i, n in enumerate(items):
            y = y_start + i * (box_h + row_gap)
            cx = x + box_w / 2
            cy = y + box_h / 2
            node_positions[n] = (cx, cy)
            info = nodes_by_name[n]
            color = TYPE_COLORS.get(info["type"], DEFAULT_COLOR)
            node_layer.append(f'<rect x="{x}" y="{y}" width="{box_w}" height="{box_h}" rx="6" fill="{color["bg"]}" stroke="{color["border"]}" stroke-width="1.5"/>')
            node_layer.append(f'<text x="{x + 12}" y="{cy + 1}" font-family="system-ui,sans-serif" font-size="13" fill="{color["text"]}" font-weight="600">{_h(info["name"][:22])}</text>')

    # Column headers
    for ci, c in enumerate(used_cols):
        x = pad_x + ci * col_width + col_gap / 2
        label_c = ""
        type_for_col = None
        for t in TYPE_ORDER:
            if type_col.get(t) == c:
                label_c = OBJECT_TYPE_LABELS.get(t, t)
                type_for_col = t
                break
        if not label_c:
            continue
        color = TYPE_COLORS.get(type_for_col, DEFAULT_COLOR) if type_for_col else DEFAULT_COLOR
        tw = len(label_c) * 9 + 20
        th = 22
        cx = x + box_w / 2
        col_headers.append(f'<rect x="{cx - tw / 2}" y="{pad_y + 4}" width="{tw}" height="{th}" rx="11" fill="{color["badge"]}" opacity="0.9"/>')
        col_headers.append(f'<text x="{cx}" y="{pad_y + 4 + th / 2 + 1}" text-anchor="middle" dominant-baseline="middle" font-family="system-ui,sans-serif" font-size="12" fill="#fff" font-weight="700">{_h(label_c)}</text>')

    svg_parts.extend(col_headers)

    # Arrow defs with glow filter
    arrow_id = f"a_{uuid.uuid4().hex[:6]}"
    filter_id = f"g_{uuid.uuid4().hex[:6]}"
    svg_parts.append(f'<defs><filter id="{filter_id}" x="-20%" y="-20%" width="140%" height="140%"><feGaussianBlur stdDeviation="1.5" result="blur"/><feMerge><feMergeNode in="blur"/><feMergeNode in="SourceGraphic"/></feMerge></filter><marker id="{arrow_id}" markerWidth="12" markerHeight="8" refX="12" refY="4" orient="auto"><path d="M0,0 L12,4 L0,8 Z" fill="#475569"/></marker></defs>')

    # Draw edges with orthogonal routing
    for n, info in nodes_by_name.items():
        if n not in node_positions:
            continue
        sx, sy = node_positions[n]
        for dep_name in info["deps"]:
            dep_lower = dep_name.lower()
            if dep_lower not in node_positions:
                continue
            dx, dy = node_positions[dep_lower]

            if dx > sx:
                x1 = sx + box_w / 2
                y1 = sy
                x4 = dx - box_w / 2
                y4 = dy
                mx = (x1 + x4) / 2
                path = f"M{x1},{y1} L{mx},{y1} L{mx},{y4} L{x4},{y4}"
            else:
                mx = (sx + dx) / 2
                x1 = sx - box_w / 2
                y1 = sy
                x4 = dx + box_w / 2
                y4 = dy
                path = f"M{x1},{y1} L{mx},{y1} L{mx},{y4} L{x4},{y4}"

            edge_layer.append(f'<path d="{path}" fill="none" stroke="#475569" stroke-width="2.5" stroke-linejoin="round" filter="url(#{filter_id})" marker-end="url(#{arrow_id})"/>')

    svg_parts.extend(edge_layer)
    svg_parts.extend(node_layer)
    svg_parts.append("</svg>")
    return "\n".join(svg_parts)


